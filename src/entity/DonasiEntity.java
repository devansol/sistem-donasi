package entity;

/**
 *
 * @author Daus
 */
public class DonasiEntity {
    private String no_donasi;
    private String nama_donatur;
    private String alamat_donatur;
    private String bukti_pembayaran;
    private double jumlah_donasi;
    private String no_telp;
    private String keterangan;

    /**
     * @return the no_donasi
     */
    public String getNo_donasi() {
        return no_donasi;
    }

    /**
     * @param no_donasi the no_donasi to set
     */
    public void setNo_donasi(String no_donasi) {
        this.no_donasi = no_donasi;
    }

    /**
     * @return the nama_donatur
     */
    public String getNama_donatur() {
        return nama_donatur;
    }

    /**
     * @param nama_donatur the nama_donatur to set
     */
    public void setNama_donatur(String nama_donatur) {
        this.nama_donatur = nama_donatur;
    }

    /**
     * @return the alamat_donatur
     */
    public String getAlamat_donatur() {
        return alamat_donatur;
    }

    /**
     * @param alamat_donatur the alamat_donatur to set
     */
    public void setAlamat_donatur(String alamat_donatur) {
        this.alamat_donatur = alamat_donatur;
    }

    /**
     * @return the bukti_pembayaran
     */
    public String getBukti_pembayaran() {
        return bukti_pembayaran;
    }

    /**
     * @param bukti_pembayaran the bukti_pembayaran to set
     */
    public void setBukti_pembayaran(String bukti_pembayaran) {
        this.bukti_pembayaran = bukti_pembayaran;
    }

    /**
     * @return the jumlah_donasi
     */
    public double getJumlah_donasi() {
        return jumlah_donasi;
    }

    /**
     * @param jumlah_donasi the jumlah_donasi to set
     */
    public void setJumlah_donasi(double jumlah_donasi) {
        this.jumlah_donasi = jumlah_donasi;
    }

    /**
     * @return the no_telp
     */
    public String getNo_telp() {
        return no_telp;
    }

    /**
     * @param no_telp the no_telp to set
     */
    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    /**
     * @return the keterangan
     */
    public String getKeterangan() {
        return keterangan;
    }

    /**
     * @param keterangan the keterangan to set
     */
    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }
    
    
    
}
