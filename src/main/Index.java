    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        AdminEntity loginEntity = new AdminEntity();
        try{
            if(inpUsername.getText().equals("")){
                inpUsername.requestFocus();
                throw new Exception("Username belum di isi !");
            }else if(inpPassword.getText().equals("")){
                inpPassword.requestFocus();
                
                throw new Exception("Password belum di isi !");
            }else{
                loginEntity.setUsername(inpUsername.getText());
                loginEntity.setPassword(inpPassword.getText());
                this.login(loginEntity);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

