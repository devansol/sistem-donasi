    private void login(AdminEntity param) throws Exception {
        try{
            AdminEntity session = new AdminEntity();
            session = adminService.login(param);
            if(session.getNama_lengkap().equals("") && session.getStatus() == 0){
               throw new Exception("Username / Password anda salah !");
            }else{
                JOptionPane.showMessageDialog(null, "Login berhasil");
                SessionEntity.setNama_lengkap(session.getNama_lengkap());
                SessionEntity.setStatus(session.getStatus());
                Dashboard ds = new Dashboard();
                ds.setVisible(true);
                ds.setTitle("Dashboard");
                this.setVisible(false);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Login gagal ! " +  e.getMessage());
        }
                
    }
            
    
