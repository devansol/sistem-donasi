    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        try{
            if(namaAdopsi.getText().equals("")){
                throw new Exception("Nama Lengkap tidak boleh kosong !");
            }else if(alamatDonatur.getText().equals("")){
                throw new Exception ("Alamat tidak boleh kosong !");
            }else if(noTelepon.getText().equals("")){
                throw new Exception("No Telepon tidak boleh kosong !");
            }else if(pekerjaan.getSelectedIndex() == 0){
                throw new Exception("Pekerjaan tidak boleh kosong !");
            }else if(namaAnak.getSelectedIndex() == 0){
                throw new Exception("Nama Anak belum di pilih !");
            }else{
                int flag = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menyimpan data ini ? ","Konfirmasi",JOptionPane.YES_NO_OPTION);
                if(flag == 0){
                    AdopsiEntity param = new AdopsiEntity();
                    param.setNo_adopsi(noAdopsi.getText());
                    param.setNama_adopsi(namaAdopsi.getText());
                    param.setAlamat(alamatDonatur.getText());
                    param.setNo_telp(noTelepon.getText());
                    param.setPekerjaan(pekerjaan.getSelectedItem().toString());

                    String[] kd_anak = namaAnak.getSelectedItem().toString().split("-");

                    param.setKode_anak(kd_anak[0]);
                    param.setKeterangan(keterangan.getText());

                    Map<String,Object> response = service.ubahDataAdopsi(param);

                    if(!(boolean) response.get("status")){
                        throw new Exception(response.get("message").toString());
                    }else{
                        JOptionPane.showMessageDialog(null,response.get("message").toString());
                        FormAdopsi form = new FormAdopsi();
                        this.setVisible(false);
                        form.setVisible(true);
                        form.setTitle("Form Donasi");
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_simpanActionPerformed

