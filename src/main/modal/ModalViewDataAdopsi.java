    private void getDataAdopsiByNomorAdopsi() throws Exception {
        AdopsiEntity response = new AdopsiEntity();
        try{
            response = service.getDataAdopsiByNoAdopsi(SessionEntity.getNo_adopsi());
            noAdopsi.setText(response.getNo_adopsi());
            namaAdopsi.setText(response.getNama_adopsi());
            alamatDonatur.setText(response.getAlamat());
            noTelepon.setText(response.getNo_telp());
            pekerjaan.setSelectedItem(response.getPekerjaan());
            
            this.getDataAnak();
            namaAnak.setSelectedItem(response.getKode_anak()+"-"+response.getNama_anak());
            keterangan.setText(response.getKeterangan());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    private void getDataAnak() throws Exception {
        List<AnakEntity> anakEntity = new ArrayList<AnakEntity>();
        try {
            anakEntity = serviceAnak.getDataAnak();
            namaAnak.removeAllItems();
            namaAnak.addItem("--Pilih--");
            for (AnakEntity entity : anakEntity) {
                namaAnak.addItem(entity.getKode_anak() + "-" + entity.getNama_anak());
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

