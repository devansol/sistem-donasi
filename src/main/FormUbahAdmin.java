    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{

            if(inpPassword.getText().length() == 0){
                inpPassword.requestFocus();
                throw new Exception("Password tidak boleh kosong !");
            } 
            
            if(inpPassword.getText().length() > 16){
                inpPassword.requestFocus();
                throw new Exception("Password tidak boleh lebih dari 16 Karakter !");
            }
            
            if(!inpPassword.getText().equals(inpConfirmPassword.getText())){
                throw new Exception("Password tidak sesuai !");
            }else{
                int flag = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menyimpan data ini ? ", "Peringatan", JOptionPane.YES_NO_OPTION);

                if(flag == 0){
                    AdminEntity param = new AdminEntity();
                    param.setUsername((String) slcUsername.getSelectedItem());
                    param.setPassword(inpPassword.getText());
                    boolean update = service.ubahPassword(param);
                    if(update){
                        JOptionPane.showMessageDialog(null, "Data berhasil di ubah !");
                        JOptionPane.showMessageDialog(null, "Silahkan melakukan login kembali !");
                        SessionEntity.setNama_lengkap("");
                        SessionEntity.setStatus(0);
                        Index idx = new Index();
                        idx.setTitle("Form Login");
                        idx.setVisible(true);
                        this.setVisible(false);
                    }else{
                        JOptionPane.showMessageDialog(null, "Data gagal ubah !");
                    }
                }
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

